iae = input("digite algo:")
print(iae.isnumeric()) #ve se é numero true ou false(verdadeiro ou falso)
print(iae.isalpha()) #ve se é texto(nao popde dar espaço)
print(iae.isalnum()) #
print(iae.isupper()) #pra ver se só tem letra maiúscula
print(iae.isdecimal())#verifica se é numero decimal
print(iae.islower()) #ve se tem minuscula
print(iae.isspace()) #ve se tem espaço